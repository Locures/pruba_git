# Alineando la condicional
# Escribe un programa que tome un número entero ingresado por el usuario y muestre "Es positivo" si el
# número es mayor que cero, de lo contrario, muestra "No es positivo".

num = int(input("ingrese un numero: "))
if num > 0:
    print(" el numero ingresado es posito")
else:
    print("el numero ingresado no es positivo")

